package kr.co.soldesk.beans;

public class OceanofSurfaceBean {
	
	private String ofsweatherID;
	private String city;
	private String years;
	private String figures;
	
	public String getOfsweatherID() {
		return ofsweatherID;
	}
	public void setOfsweatherID(String ofsweatherID) {
		this.ofsweatherID = ofsweatherID;
	}
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	public String getYears() {
		return years;
	}
	public void setYears(String years) {
		this.years = years;
	}
	public String getFigures() {
		return figures;
	}
	public void setFigures(String figures) {
		this.figures = figures;
	}
	
	
	
}
